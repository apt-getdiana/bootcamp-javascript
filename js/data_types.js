            //Types of variables
            //Number-> var variable = number;
            var variable = 0;
            console.log(variable);
            console.log(typeof variable);
            //String -> var variable = "number";
            variable = "hola a todos";
            console.log(variable);
            console.log(typeof variable);
            // Boolean -> var variable = true | false;
            variable = true;
            console.log(variable);
            console.log(typeof variable);
            /*Object -> var variable = {
                property: value,
                property2: value2
            };*/
            variable = {};
            console.log(variable);
            console.log(typeof variable);
            // array -> var variable = [value, value2, ..., value_n]
            variable = [];
            console.log(variable);
            console.log(typeof variable);
            // null -> var variable = null
            variable = null;
            console.log(variable);
            console.log(typeof variable);
            // undefined -> var variable = undefined
            variable = undefined;
            console.log(variable);
            console.log(typeof variable);